window.getLocaleString = function(key, lang) {
  let cleanLang = 'en';
  if (lang) {
    const mainLang = lang.split('-')[0].toLowerCase();
    if (window.saveFilterLocales[mainLang]) {
      cleanLang = mainLang;
    }
  }
  return window.saveFilterLocales[cleanLang][key] || window.saveFilterLocales['en'][key] || key;
};
